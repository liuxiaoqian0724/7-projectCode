@Controller
/**
 * 
 * spring 默认为singleton模式即单例模式，设置成这个可以避免线程安全问题
 */
@Scope("prototype")
/**
 * @CrossOrigin 跨域请求开启
 */
@RequestMapping("/user")
@CrossOrigin
@ResponseBody
public class LogOutController {
	private PrintWriter writer;
	@RequestMapping(value = "/logout", method = RequestMethod.POST)
	public void logout(HttpServletResponse response, HttpServletRequest request) {
		/**
		 * 设置跨域请求
		 */
		response.setHeader("Access-Control-Allow-Origin", "*");
		response.setHeader("Access-Control-Allow-Methods", "POST");
		response.setHeader("Access-Control-Allow-Headers", "x-requested-with,content-type");
		response.setContentType("text/html;charset=utf-8");
		response.setCharacterEncoding("utf-8");
		Cookie jessionid = new Cookie("JESSIONID", "");
		Cookie EMAIL = new Cookie("EMAIL","");
		Cookie USERNAME = new Cookie("USERNAME","");
		Cookie ROLE = new Cookie("ROLE","");
		jessionid.setPath(request.getContextPath() + "/");
		EMAIL.setPath(request.getContextPath() + "/");
		USERNAME.setPath(request.getContextPath() + "/");
		ROLE.setPath(request.getContextPath() + "/");
		jessionid.setMaxAge(0);
		EMAIL.setMaxAge(0);
		USERNAME.setMaxAge(0);
		ROLE.setMaxAge(0);
		response.addCookie(jessionid);
		response.addCookie(EMAIL);
		response.addCookie(USERNAME);
		response.addCookie(ROLE);
		try {
			writer = response.getWriter();
			writer.write("ok");
			writer.flush();
			writer.close();
		} catch (IOException e) {
			writer.close();
		}
	}
}
